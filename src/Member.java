public class Member {
    int idMember;
    String namaMember;

    public Member(int idMember, String namaMember) {
        this.idMember = idMember;
        this.namaMember = namaMember;
    }

    public int getIdMember() {
        return idMember;
    }

    public String getNamaMember() {
        return namaMember;
    }
}
